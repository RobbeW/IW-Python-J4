def write_yaml( data:list ):
    """ A function to write YAML file"""
    with open(os.path.join('..', 'evaluation', 'tests.yaml'), 'w', encoding='utf-8') as f:
        yaml.dump(data, f)


module_name = 'solution'
file_path = os.path.join(solutiondir, 'solution.nl.py')
spec = importlib.util.spec_from_file_location(module_name, file_path)
module = importlib.util.module_from_spec(spec)
spec.loader.exec_module(module)

def construct_palindrome( a ):
    n = a // 2
    start = random.randint(10**(n-1), 10**(n)-1)
    orig = start
    nieuw = 0
    while start // 1 !=0:
        eenheid = start % 10
        nieuw = nieuw*10 + eenheid
        start //= 10

    print(a, orig, nieuw)
    return orig* (10**math.ceil(a/2)) + nieuw

# generate test data
ntests= 30
cases = [(1221,),(14541,),(13321,),(99,),(5,)]
while len(cases) < ntests:
    flag = random.randint(0,1)
    if flag == 1:
        test = construct_palindrome( random.randint(2,15) )
    else:
        test = random.randint(10,10**10)
    cases.append( (test,))

cases.sort()

# generate unit tests for functions
yamldata = []

# input, expression, statement or stdin?
input = 'stdin'
# output, stdout or return?
output = 'stdout'
tabtitle = "Feedback"

yamldata.append( {'tab': tabtitle, 'contexts': []})

for i in range(len(cases)):
    test = cases[i]
    yamldata[0]['contexts'].append( {'testcases' : []})
    # generate test expression
    # add input to input file
    stdin = '\n'.join(f'{line}' for line in test)

    # generate output to output file
    script = os.path.join(solutiondir, 'solution.nl.py')
    process= subprocess.run(
        ['python3', script],
        input=stdin,
        encoding='utf-8',
        capture_output=True
    )
    
    result_lines = process.stdout.split("\n")
    result_lines = [x for x in result_lines[:-1]] ## drop last element
    
    outputtxt = ""
    for line in result_lines:
        if not(line.startswith( 'Geef' )):
            print(line)
            outputtxt += line
            
    testcase = { input: stdin, output: outputtxt }            
    yamldata[0]['contexts'][i]["testcases"].append( testcase)
    
    # generate test expression
    #
    expression_name = f"is_palindroom( {test[0]} )"
    result = module.is_palindroom( test[0] )

    print(result)
    # setup for return expressions
    testcase = { "expression": expression_name, "return": result }
    yamldata[0]['contexts'][i]["testcases"].append( testcase)

